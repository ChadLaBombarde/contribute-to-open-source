exports._check = (...args) => {
  args.forEach((arg) => {
    if (typeof arg !== 'number') {
      throw new TypeError(`${arg} is not a number`);
    }
  });
};

exports.add = (x, y) => {
  exports._check(x, y);
  return x + y;
};

exports.subtract = (x, y) => {
  exports._check(x, y);
  return x - y;
};

exports.multiply = (x, y) => {
  exports._check(x, y);
  return x * y;
};

exports.divide = (x, y) => {
  exports._check(x, y);
  return x / y;
};

module.exports = exports;
